/**
 * Create icon element with optional wrapper
 * @param {string} name - Icon name from icons object
 * @param {object} options - Configuration options
 * @returns {string} HTML string for the icon
 */
export function getIcon(name, options = {}) {
    const icon = icons[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }
    
    const { className = '', ariaLabel = '' } = options;
    
    if (className || ariaLabel) {
        const wrapper = document.createElement('span');
        wrapper.className = `icon ${className}`.trim();
        wrapper.innerHTML = icon;
        if (ariaLabel) {
            wrapper.setAttribute('aria-label', ariaLabel);
            wrapper.setAttribute('role', 'img');
        } else {
            wrapper.setAttribute('aria-hidden', 'true');
        }
        return wrapper.outerHTML;
    }
    
    return icon;
}

